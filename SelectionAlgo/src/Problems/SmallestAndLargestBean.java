package Problems;

public class SmallestAndLargestBean {

	private int small;
	
	private int large;

	public int getSmall() {
		return small;
	}

	public void setSmall(int small) {
		this.small = small;
	}

	public int getLarge() {
		return large;
	}

	public void setLarge(int large) {
		this.large = large;
	}

	@Override
	public String toString() {
		return "SmallestAndLargestBean [small=" + small + ", large=" + large + "]";
	}
	
}
